# practice  — short training session
# ---------------------------------------------------------------------------
def cmd_practice(args: argparse.Namespace) -> None:
    from .model import GPT
    from .optim import SGD
    from .tokenize import ByteTokenizer

    steps = int(args.steps)
    corpus_dir = Path(args.corpus) if args.corpus else Path.cwd() / "corpus"

    # Find or create a tiny corpus
    texts = []
    if corpus_dir.is_dir():
        for f in sorted(corpus_dir.glob("*.txt")):
            texts.append(f.read_text(encoding="utf-8", errors="replace"))
    if not texts:
        texts = ["hello ultra graph world. this is a tiny practice corpus. " * 20]

    corpus = "\n".join(texts)
    tok = ByteTokenizer()
    ids = tok.encode(corpus)

    # Tiny GPT
    model = GPT(vocab=256, d_model=32, n_layers=2, n_heads=2, max_len=64)
    opt = SGD(model, lr=0.1, momentum=0.9)

    prev_loss = float("inf")
    for step in range(steps):
        chunk = ids[step % (len(ids) - 64): step % (len(ids) - 64) + 64]
        inp = chunk[:-1]
        tgt = chunk[1:]
        logits = model(inp)
        loss = logits.cross_entropy(tgt)
        opt.zero_grad()
        loss.backward()
        opt.step()
        loss_val = float(loss.data)

        if step % 10 == 0 or step == steps - 1:
            delta = f" ({'↓' if loss_val < prev_loss else '↑'})" if step > 0 else ""
            print(f"  step {step:>4}  loss {loss_val:.4f}{delta}")
            prev_loss = loss_val

    sample = tok.decode(model.generate([72, 105], n_new=32, temperature=0.8))
    print(f"\n✓ {steps} steps complete")
    print(f"  Final loss: {loss_val:.4f}")
    print(f"  Sample: {sample[:80]}...")


