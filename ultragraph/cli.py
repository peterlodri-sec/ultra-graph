def main(argv: list[str] | None = None) -> None:
    parser = argparse.ArgumentParser(prog="ug", description="ultragraph CLI")
    sub = parser.add_subparsers(dest="command", required=True)

    p_pull = sub.add_parser("pull", help="Download and verify a deployed model")
    p_pull.add_argument("name", help="Model name (e.g. hungarian)")
    p_pull.add_argument("--url", help="Override download URL")
    p_pull.add_argument("--dir", default="examples/data", help="Output directory")

    p_new = sub.add_parser("new", help="Scaffold a new project")
    p_new.add_argument("name", help="Project name")
    p_new.add_argument("--lang", default="english", help="Target language")

    p_report = sub.add_parser("report", help="Generate HTML report from checkpoint")
    p_report.add_argument("checkpoint", help="Path to .npz checkpoint")

    p_practice = sub.add_parser("practice", help="Short training session")
    p_practice.add_argument("--steps", default=100, help="Number of training steps")
    p_practice.add_argument("--corpus", help="Path to corpus directory")

    p_breed = sub.add_parser("breed", help="Average two deployed checkpoints")
    p_breed.add_argument("model_a", help="First deployed checkpoint")
    p_breed.add_argument("model_b", help="Second deployed checkpoint")
    p_breed.add_argument("--ratio", type=float, default=0.5, help="Blend ratio (0 = pure A, 1 = pure B)")
    p_breed.add_argument("--output", "-o", help="Output path")

    p_compile = sub.add_parser("compile", help="Compile model to .ugm")
    p_compile.add_argument("model", help="Deployed .npz checkpoint")
    p_compile.add_argument("target", nargs="?", default="ugm", help="Target format (ugm, wasm, rp2040)")

    p_publish = sub.add_parser("publish", help="Publish a deployed model to the hub")
    p_publish.add_argument("file", help="Path to .npz or .ugm model")
    p_publish.add_argument("--name", help="Model name (defaults to filename)")
    p_publish.add_argument("--lang", default="unknown", help="Model language")
    p_publish.add_argument("--author", default="anonymous", help="Author name")
    p_publish.add_argument("--notes", default="", help="Free-form notes")

    p_search = sub.add_parser("search", help="Search models in the hub registry")
    p_search.add_argument("query", nargs="?", default="", help="Search query (language, name, author)")
    p_search.add_argument("--list", action="store_true", help="List all available models")

    p_spar = sub.add_parser("spar", help="Compare two models on the same prompt")
    p_spar.add_argument("model_a", help="First model (.npz)")
    p_spar.add_argument("model_b", help="Second model (.npz)")
    p_spar.add_argument("--prompt", default="Hello world", help="Prompt text")
    p_spar.add_argument("--n", type=int, default=32, help="Tokens to generate")

    p_taste = sub.add_parser("temperature", help="Sample at different temperatures")
    p_taste.add_argument("model", help="Deployed model (.npz)")
    p_taste.add_argument("--prompt", default="The", help="Prompt text")
    p_taste.add_argument("--n", type=int, default=48, help="Tokens per sample")

    p_run = sub.add_parser("run", help="Execute a .ugm module")
    p_run.add_argument("file", help="Path to .ugm file")
    p_run.add_argument("--info", action="store_true", help="Print module info only")
    p_run.add_argument("--jit", action="store_true", help="Profile and suggest JIT optimizations")
    p_run.add_argument("--record", action="store_true", help="Record activation history and save")

    p_history = sub.add_parser("history", help="Inspect activation history in .ugm")
    p_history.add_argument("file", help="Path to .ugm file")
    p_history.add_argument("--clear", action="store_true", help="Clear history segment")

    p_link = sub.add_parser("link", help="Link two .ugm modules")
    p_link.add_argument("model_a", help="First .ugm module")
    p_link.add_argument("model_b", help="Second .ugm module")
    p_link.add_argument("--mode", default="sequential", help="Link mode: sequential, sum, avg, max")
    p_link.add_argument("--output", "-o", help="Output .ugm path")

    args = parser.parse_args(argv)
    if args.command == "pull":
        cmd_pull(args)
    elif args.command == "new":
        cmd_new(args)
    elif args.command == "report":
        cmd_report(args)
    elif args.command == "practice":
        cmd_practice(args)
    elif args.command == "breed":
        cmd_breed(args)
    elif args.command == "compile":
        cmd_compile(args)
    elif args.command == "link":
        cmd_link(args)
    elif args.command == "run":
        if args.jit:
            _cmd_run_jit(args)
        elif args.record:
            from .ugm import load_ugm, save_ugm
            module = load_ugm(Path(args.file))
            in_dim = module.trees[0].in_dim if module.trees else 1
            x = np.random.randn(1, in_dim).astype(np.float32)
            _record_history(module, x)
            save_ugm(Path(args.file), module)
            print(f"✓ Recorded history to {args.file}")
        else:
            cmd_run(args)
    elif args.command == "history":
        cmd_history(args)
    elif args.command == "publish":
        cmd_publish(args)
    elif args.command == "search":
        cmd_search(args)
    elif args.command == "spar":
        cmd_spar(args)
    elif args.command == "temperature":
        cmd_taste(args)


if __name__ == "__main__":
    main()
