# breed  — average two deployed checkpoints
# ---------------------------------------------------------------------------
def cmd_breed(args: argparse.Namespace) -> None:
    import json as _json

    a_path = Path(args.model_a)
    b_path = Path(args.model_b)
    if not a_path.exists():
        print(f"✗ {a_path} not found", file=sys.stderr)
        raise SystemExit(1)
    if not b_path.exists():
        print(f"✗ {b_path} not found", file=sys.stderr)
        raise SystemExit(1)

    ratio = float(args.ratio) if args.ratio is not None else 0.5
    if not (0.0 <= ratio <= 1.0):
        print(f"✗ ratio must be in [0, 1], got {ratio}", file=sys.stderr)
        raise SystemExit(1)

    a_data = np.load(a_path, allow_pickle=False)
    b_data = np.load(b_path, allow_pickle=False)

    # Use A's meta as base, merge hp names
    a_meta = _json.loads(str(a_data["__meta__"]))
    merged_hp = dict(a_meta.get("hp", {}))
    merged_hp["parents"] = [a_path.name, b_path.name]
    merged_hp["breed_ratio"] = ratio

    arrays = {}
    for key in a_data.files:
        if key == "__meta__":
            continue
        if key not in b_data.files:
            print(f"  ⚠ key {key} only in parent A, using as-is")
            arrays[key] = a_data[key]
            continue
        a_arr = a_data[key].copy()
        b_arr = b_data[key]
        if a_arr.shape != b_arr.shape:
            print(f"  ⚠ shape mismatch for {key}: {a_arr.shape} vs {b_arr.shape}, using A")
            arrays[key] = a_arr
        else:
            arrays[key] = (a_arr * (1 - ratio) + b_arr * ratio).astype(a_arr.dtype)

    # Default output name
    a_stem = a_path.stem.replace(".q", "")
    b_stem = b_path.stem.replace(".q", "")
    out_name = args.output or f"{a_stem}-x-{b_stem}.npz"
    out_path = Path(out_name)
    arrays["__meta__"] = np.array(_json.dumps({**a_meta, "hp": merged_hp}))
    np.savez(out_path, **arrays)

    size = out_path.stat().st_size
    print(f"✓ Bred {a_path.name} × {b_path.name} (ratio={ratio}) → {out_path} ({size / 1024:.0f} KB)")


# ---------------------------------------------------------------------------
# compile  — compile a model to .ugm or other target
# ---------------------------------------------------------------------------
def cmd_compile(args: argparse.Namespace) -> None:
    from .model import GPT
    from .ugm import from_ultragraph, save_ugm

    model_path = Path(args.model)
    target = args.target
    data = np.load(model_path, allow_pickle=False)

    if "__meta__" in data.files:
        model = GPT.load_deployed(str(model_path))
    else:
        print("⚠ Training checkpoint detected; need deployed .npz", file=sys.stderr)
        raise SystemExit(1)

    if target == "ugm" or target.endswith(".ugm"):
        out = Path(target) if target.endswith(".ugm") else model_path.with_suffix(".ugm")
        module = from_ultragraph(model)
        save_ugm(out, module)
        print(f"✓ Compiled {model_path.name} → {out} ({out.stat().st_size / 1024:.0f} KB)")
    elif target == "wasm" or target.endswith(".wasm"):
        from .wasm import save_wasm as _save_wasm

        out = Path(target) if target.endswith((".wasm", ".wat")) else model_path.with_suffix(".wasm")
        module = from_ultragraph(model)
        _save_wasm(module, out)
    elif target == "rp2040" or target.endswith(".c"):
        from .rp2040 import save_rp2040 as _save_rp2040

        module = from_ultragraph(model)
        out = Path(target) if target.endswith(".c") else model_path.with_suffix(".c")
        _save_rp2040(module, out)
    else:
        print(f"✗ Unknown target: {target} (supported: ugm, wasm, rp2040)", file=sys.stderr)
        raise SystemExit(1)


